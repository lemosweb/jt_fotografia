            <p>Julio Torres é um documentarista paulistano, morador do Grajaú no extremo Sul da cidade de Sao Paulo e interessado em retratar o humano. Seu olhar crítico aliado a necessidade de expressar as imagens que carrega consigo desde muito antes de se tornar fotógrafo o impulsiona a entrar em contato com a arte urbana e fotográfica. A partir de vivencias em oficinas culturais em um Centro de Defesa dos Direitos da Criança e do Adolescente (CEDECA Interlagos) Torres conhece a linguagem do grafite e transforma suas expressoes em arte nos muros da cidade. Logo, a necessidade de retratar seus grafites o despertou para a fotografia como meio de comunicaçao potente para retratar o cotidiano e locais por onde passava.

                Desde entao, nos últimos 4 anos Júlio tem se dedicado a documentar suas caminhadas pela metrópole paulistana, com olhar atento que encontra e tenta dar voz ao humano e urbano das periferias e centro da cidade. Nos fragmentos das composiçoes em detalhes e formas, revela-se a visao do autor que nos oferta uma fotografia crítica para leitura ampla e elevada ao estado de espirito de Torres o retratista ou seu leitor, aquele que se permite impactar por suas imagens carregadas de histórias.

                Sua produçao poética geralmente em Preto e Branco e de fortes contrastes, revela-se potente para reflexão sobre o Direito Humano básico a liberdade de Expressao e representaçao de uma cidade invisível aos olhos de muitos. Suas fotos sobre os migrantes, moradores de rua e questoes sociais podem ser interpretadas como protestos contra a desigualdade social, descaso e abandono do poder público.

                Júlio Torres é uma referencia na cidade de São Paulo pela documentação do movimento "rap", registrando seus principais personagens, eventos e grupos. O encontro destas duas linguagens, a fotográfica e o rap, também caminham juntos na medida que tentam expressar o mesmo descontentamento e ideal por uma sociedade mais justa..</p>


